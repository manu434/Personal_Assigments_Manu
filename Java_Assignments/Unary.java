package basics;

public class Unary {
 public static void main(String[] agrs) {
	 //post_increment
	 int a =60;
	 System.out.println(a++);
	 System.out.println(a);
	 
	 //post_decrement
	 int b = 60;
	 System.out.println(b--);
	 System.out.println(b);
	 
	 //pre_increment
	 int c =60;
	 System.out.println(++c);
	 System.out.println(c);
	 
	 //pre_decrement
	 int d =60;
	 System.out.println(--d);
	 System.out.println(d);
	 
 }
}
