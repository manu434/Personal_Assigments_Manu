package basics;

public class Relational {
  public static void main(String[] args)
  {
	  int a =10;
	  int b =20;
	  System.out.println("The First no: " + a);
	  System.out.println("The second no: " + b);
	  System.out.println(a>b);
	  System.out.println(a<b);
	  System.out.println(a<=b);
	  System.out.println(a>=b);
	  System.out.println(a=b);
	  System.out.println("The new value of a: " + a);
	  System.out.println(a==b);
	  System.out.println(a!=b);	  
	 
  }
}

